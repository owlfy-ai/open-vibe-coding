export type Translation = typeof zh;

export function resolveLanguage(preference: LanguagePreference): UiLanguage {
  if (preference === "zh" || preference === "en") return preference;
  if (typeof navigator !== "undefined" && navigator.language.toLowerCase().startsWith("zh")) {
    return "zh";
  }
  return "en";
}

export function dictionary(language: UiLanguage): Translation {
  return language === "zh" ? zh : en;
}

export function interpolate(template: string, values: Record<string, string | number>): string {
  return Object.entries(values).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  );
}

export function useT(): Translation {
  const { database } = useApplication();
  return dictionary(resolveLanguage(database.settings.system.language));
}
